
public class MainMethodOverloading {
	
	public static void main()
	{
		System.out.println("This is main 1 method string");
	}
	
	public static void main(String a)
	{
		System.out.println("This is main 2 method string");
	}

	public static void main(String[] args) {
		System.out.println("This is my main method Java Code");

	}
	
	public static void main(int[] args) {
		System.out.println("This is my main 3 method string");

	}


}
